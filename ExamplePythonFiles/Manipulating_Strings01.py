

# Using Upper, Lower and title
curso = "pYtHon"

print(curso.upper())
print(curso.lower())
print(curso.title())


# Eliminating white space

curso = "   Python "

print(curso.strip())
print(curso.lstrip())
print(curso.rstrip())


# Separate and centralize
curso = "Python"

print(curso.center(10, "#"))
print(".".join(curso))

